package base.engineering.motosumiyoshi.sakeapp.model;

public class LinePoint {

    private final int x;

    private final int y;

    public LinePoint(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
